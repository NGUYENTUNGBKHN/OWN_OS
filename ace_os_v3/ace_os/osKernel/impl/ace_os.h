/*
------------------------------------------------------------------------------------------------------------------------
*                                                      ERROR CODES
------------------------------------------------------------------------------------------------------------------------
*/
typedef enum ace_os_err_s 
{
    ACE_OS_ERR_NONE         =       0u,
    ACE_OS_ERR_A            =   10000u,
    ACE_OS_TCB_INVALID      =   20000u,
}ace_os_err;


/*
************************************************************************************************************************
************************************************************************************************************************
*                                          D A T A   S T R U C T U R E S
************************************************************************************************************************
************************************************************************************************************************
*/

/*
------------------------------------------------------------------------------------------------------------------------
*                                                      READY LIST
------------------------------------------------------------------------------------------------------------------------
*/
struct ace_os_rdy_list_s
{
    ace_os_tcb      *HeadPtr;               /* Pointer to task that will run at selected priority */
    ace_os_tcb      *TailPtr;               /* Pointer to last task          at selected priority */
    uint16_t        NbrEntries;             /* Number of entries             at selected priority */
};


/*
------------------------------------------------------------------------------------------------------------------------
*                                                  TASK CONTROL BLOCK
------------------------------------------------------------------------------------------------------------------------
*/
struct ace_os_tcb_s
{
    uint32_t            *StkPtr;                /* Pointer to current top of stack */

    ace_os_tcb          *NextPtr;               /* Pointer to next TCB in the TCB list */
    ace_os_tcb          *PrevPtr;               /* Pointer to previous TCB in the TCB list*/

    uint32_t            *StkBasePtr;            /* Pointer to base address of stack */
    uint32_t            Stk_size;               /* Size of stack */

    ace_os_task_func    TaskEntryAddr;         /* Pointer to task entry point address */
    void                *TaskEntryArg;          /* Argument passed to task when it was created */

    uint32_t            TimeQuanta;             /* Time slice allocation (in ticks) */
    uint32_t            TimeQuantaCtr;          /* Time slice remaining (in ticks) */

    uint8_t             TaskState;
    uint8_t             Prio;
};

